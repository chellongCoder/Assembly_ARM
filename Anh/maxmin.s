; maxmin 

  AREA RESET, DATA, READONLY
     DCD 0x20001000
	 DCD ST
  ALIGN
CHUOI DCD 1,2,2,3,5
  AREA LINH, CODE, READONLY
    ENTRY
	EXPORT ST
;HAM CON TINH TONG CHUOI
SUMCHUOI PROC 
    ADD R4,R4,#1
   CMP R4,#6

   BLT SUM
    BX LR 
   ENDP 
SUM 
    LDR R5,[R1,R2]
   ADD R3,R3,R5
NHAYPT
   ADD R2,R2,#4
   b SUMCHUOI

; TIM MAX MIN TRONG CHUOI

MAXMIN PROC 

   LDR R5,[R1,R2]

KIEMTRA 
   CMP R9,#0
   BEQ GANTEMP
   
KIEMTRA1 
   CMP R5,R7
   BGT GANMAX
   BLT MIN
   BEQ CHUYENPT
GANMAX 
   MOV R7,R5
   B CHUYENPT
MIN 
   CMP R5,R8
   BLT GANMIN
   BGE CHUYENPT 
GANMIN
    MOV R8,R5
	B CHUYENPT
   
GANTEMP
   MOV R7,R5
   MOV R8,R5
   ADD R9,R9,#1
   
CHUYENPT
    ADD R2,R2,#4
	ADD R4,R4,#1
	CMP R4,#5
	BLT MAXMIN
	BX LR
	ENDP
	
; MAIN
ST
   LDR R1,=CHUOI
   MOV R2,#0 ; DIA CHI OFFSET
   MOV R3,#0 ; SUM
   MOV R4,#0 ; BO DEM SO PHAN TU TRONG CHUOI
   MOV R5,#0 ; GIA TRI
   BL SUMCHUOI
   MOV R2,#0 ; DIA CHI OFFSET
   MOV R4,#0 ; BO DEM SO PHAN TU TRONG CHUOI
   MOV R5,#0 ; GIA TRI
   MOV R7,#0 ; MAX
   MOV R8,#0 ;MIN
   MOV R9,#0 ; TEMP
   BL MAXMIN
   
STOP B STOP
   END 