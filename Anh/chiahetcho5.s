; tinh tong chia het cho 5
  AREA RESET,DATA,READONLY
      DCD 0x20001000
	  DCD ST
	 ALIGN
MATRAN 
     DCD 1,2,3
	 DCD 5,10,5
	 DCD 3,5,9
	
  AREA LINH, CODE ,READONLY
    ENTRY
	 EXPORT ST
; HAM CON 
CHIAHET

   ADD R3,R3,#1
   
    CMP R3,#9
	BLE KIEMTRA
	BX LR 
	ENDP
KIEMTRA
   LDR R2,[R0,R1]
   UDIV R6,R2,R5
   MUL R7,R6,R5
   CMP R7,R2
   BEQ SUM
   BNE NHAYPT
SUM
   ADD R4,R4,R2
   
   B NHAYPT
NHAYPT
  ADD R1,R1,#4
  
  B CHIAHET

   

;MAIN
ST
   LDR R0,=MATRAN
   MOV R1,#0 ;DIA CHI OFFSET
   MOV R2,#0 ;GIATRI
   MOV R3,#0 ; BIEM DEM PHAN TU
   MOV R4,#0 ;SUM
   MOV R5,#5 ; SO CHIA
	BL CHIAHET 
STOP B STOP
 END 