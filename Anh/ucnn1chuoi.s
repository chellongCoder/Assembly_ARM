 AREA RESET, DATA, READONLY
     DCD 0x20001000
	 DCD ST
	 ALIGN
MATRAN 
   DCD 4,8,24
  AREA LINH, CODE, READONLY
     ENTRY
	 EXPORT ST
; TIM UCNN CUA  1 CHUOI
UCNN PROC 
   LDR R5,[R0,R1]
   MOV R2,R5
SOTT  
   ADD R1,R1,#4
   LDR R6,[R0,R1]
KIEMTRA
   CMP R2,R6
   BEQ GANUCNN
   BNE TRU

TRU 
   CMP R2,R6
   BLT TRU1
   BGT TRU2
TRU1 
   SUB R6,R6,R2
   B KIEMTRA
TRU2
   SUB R2,R2,R6
   B KIEMTRA
GANUCNN
   MOV R2,R6
   B NHAYPT  
NHAYPT
   
   ADD R4,R4,#1
   CMP R4,#2
   BLT SOTT
   BX LR
   ENDP
   

; MAIN
ST 
   LDR R0,=MATRAN
   MOV R1,#0 ; DIACHI OFFSET
   MOV R2,#0 ;UCNN 
   MOV R3,#0 ; MIN
   MOV R4,#0 ; BO DEM PHAN TU 
   MOV R5,#0 ; DIA CHI DAU TIEN
   BL UCNN
STOP B STOP 
   END 